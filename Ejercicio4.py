#Area y longitud de un círculo

radio = float(input("Digite el radio del círculo: "))

area = (3.1416 *radio**2)
longitud = (2*3.1416*radio)

print(f"El area del círculo es: {area: .2f}")
print(f"La longitud del círculo es: {longitud: .2f}") #El .2f son los decimales que quieres mostrar en el resultado

